//**************************************FUNCTION DEFINITION********************************************
// Delay Function.
//*****************************************************************************************************
void delay(int cnt)
{
    int i;
    for(i=0;i<cnt;i++);
}
//*****************************************************************************************************
//**********************************************END****************************************************

//**************************************FUNCTION DEFINITION********************************************
// Millisecond Delay Function.
//*****************************************************************************************************
void delay_ms(unsigned int ms)
{
    unsigned int i,j;

    for(i=0;i<ms;i++)
        for(j=0;j<2500;j++);
}
//*****************************************************************************************************
//**********************************************END****************************************************
